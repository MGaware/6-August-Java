package Polymorphism;

public class Method_Overloading {

	//METHOD OVERLOADIND-DECLARING MULTIPLE METHODS WITH SAME METHOD NAME BUT DIFFERENT ARGUMENTS OR PARAMETERS IN A SAME CLASS ARE CALLED AS METHOD OVERLOADING.
	
	
	public void a1()
	{
		System.out.println("mahesh");
		
	}
	
	public void a1(int a)
	{
		
		System.out.println(52);
	}
	
	
	public void a1(String b)
	{
		
		System.out.println("gaware");
	}
	
	
	public void a1(float c) 
	{
		
		System.out.println(22.11);
	}
	
	
	public void a1(char g) 
	{
		System.out.println('A');
		
	}
	
}

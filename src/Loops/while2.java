package Loops;

public class while2 {

     //PRINT NUMBERS FROM 100 TO 57
	
	public static void main(String[]args) 
	{
		int A=100;                     //STARTING CONDITION
		
		while(A>=57)                   //END CONDITION
		{
			
			System.out.println(A);
			A=A-1;                   //DECREMENT
		}
		
		
		
	}




}

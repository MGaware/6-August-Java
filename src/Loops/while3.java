package Loops;

public class while3 {

    //PRINT ODD NUMBERS FROM 21 TO 321
	
	public static void main(String[]args) 
	{
		int b=21;                    //STARTING CONDITION
		
		while(b<=321)                //END CONDITION
		{
			
			System.out.println(b);
			
		      b=b+2;	                      //INCREMENT
			
			
		}
		
	}



}

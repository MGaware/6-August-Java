package Loops;

public class while1 {

    //PRINT NUMBER FROM 1 TO 20
	public static void main(String[]args) 
	{
		int a=1;                         //STARTING CONDITION
		
		while(a<=20)                     //END CONDITION
		{
			
			System.out.println(a);
	    	a=a+1;	                         //INCREMENT
			
		}
		
	}



}

package Loops;

public class do_while4 {

   //PRINT NUMBERS FROM 10 TO 110
	
	public static void main(String[]args) 
	{
		
		int k=10;                           //STARTING CONDITION
		
		do
		{
		 
			System.out.println(k);
			
			k=k+1;                         //INCREMENT
			
		}
		while(k<=110);                      //END CONDITION
		
		
	}



}

package Inheritance;

public class sample1 {

	
	public void m1() 
	{
		
		System.out.println("mahesh");
		
	}
	
	private void m2() 
	{
		
		System.out.println("kalyan");
		
	}
	
	void m3() 
	{
		
		System.out.println("gaware");
		
	}
	
	protected void m4() 
	{
		
		System.out.println("lata");
		
	}
	
	

	
	
	
	
	
	
	
}

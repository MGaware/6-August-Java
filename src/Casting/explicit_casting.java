package Casting;

public class explicit_casting {

	//explicit casting....>converting higher datatype information into lower datatype information.
	
	public static void main(String[] args) {
		
		
		double b=22.8;          //size-8 byte
		System.out.println(b);
		
		int c=(int) b;          //size-4 byte
		System.out.println(c);
	}
	
	
	
}

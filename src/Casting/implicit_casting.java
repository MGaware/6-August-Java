package Casting;

public class implicit_casting 
{

	//implicit casting....>converting lower datatype information into higher datatype information. 
	
	public static void main(String[] args) {
		
		int a=12;                  //int size-4 byte
		
		System.out.println(a);    
		
		double b=a;                //double size-8 byte
		System.out.println(b);
		
	}
	
	
}
